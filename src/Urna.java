    private void confirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmaActionPerformed
        switch(selectCand) {
            case "111":
                        candidato111++;
                        break;
                    case "101":
                        candidato101++;
                        break;
                    case "010":
                        candidato010++;
                        break;
        }
        obs.setText("Voto computado com sucesso.");
        selectCand = "";
        toVote.setText("");
        number1.setText("");
        number2.setText("");
        number3.setText("");
        nome1.setVisible(false);
        contribuicao.setVisible(false);
        contri1.setVisible(false);
        obsOne.setVisible(false);
        obsTwo.setVisible(false);
        foto.setIcon(new javax.swing.ImageIcon(getClass().getResource("")));
        confirma.setEnabled(false);
        digVote = 1;
        
        
    }//GEN-LAST:event_confirmaActionPerformed

