    private void numChange(String numero) {
          selectCand+=numero;
          foto.setIcon(new javax.swing.ImageIcon(getClass().getResource("")));
          obs.setText("");
          toVote.setText("<html>Vote para decidir quem foi o \"pai\" da COMPUTAÇÃO:</html>");

          if(digVote < 3){
              if(digVote == 1) {
                number1.setText(numero);
              } else if(digVote == 2) {
                number2.setText(numero);
              } 
          }
          else if(digVote == 3){
              number3.setText(numero);
              obsOne.setForeground(Color.red);
              
              switch(selectCand) {
                    case "111":
                        this.getCandidato("John Von Neumann", "Ter formalizado o projeto lógico de um computador. <br/>Em sua proposta, sugeriu que as instruções fossem armazenadas na memória do computador.", "JN.jpg");
                        break;
                    case "101":
                        this.getCandidato("Alan Turing", "Um modelo teórico que pode ser usado para implementar todos os aspectos lógicos e matemáticos de um computador.", "AT.jpg");
                        break;
                    case "010":
                        this.getCandidato("Charles Babbage", "Referenciado como o inventor que projetou o primeiro computador de uso geral, utilizando apenas partes mecânicas, a máquina analítica.", "CB.jpg");
                        break;
                }
            }  
            digVote ++;
    }
            
    
